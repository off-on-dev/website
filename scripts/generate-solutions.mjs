#!/usr/bin/env node

/**
 * Generate src/data/solutions/index.ts from authored TypeScript solution files.
 *
 * Usage:
 *   node scripts/generate-solutions.mjs
 *
 * Scans src/data/solutions/<adventure-id>/<level-id>.ts (authored files, not generated)
 * and rebuilds the barrel index plus patches GENERATED:solutions regions in
 * react-router.config.ts, e2e/smoke.spec.ts, src/test/seo.test.ts, and
 * src/test/prerender.test.ts.
 *
 * Never edit src/data/solutions/index.ts by hand.
 */

import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname, basename } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const SOLUTIONS_DIR = resolve(ROOT, "src/data/solutions");

const EXCLUDED = new Set(["index.ts", "types.ts"]);

function generateSolutions() {
  if (!existsSync(SOLUTIONS_DIR)) {
    console.error(`Solutions directory not found: ${SOLUTIONS_DIR}`);
    process.exit(1);
  }

  const entries = [];

  const adventureDirs = readdirSync(SOLUTIONS_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name);

  for (const adventureId of adventureDirs) {
    const adventureDir = resolve(SOLUTIONS_DIR, adventureId);
    const tsFiles = readdirSync(adventureDir).filter(
      (f) => f.endsWith(".ts") && !f.endsWith(".generated.ts") && !EXCLUDED.has(f)
    );

    for (const tsFile of tsFiles) {
      const levelId = basename(tsFile, ".ts");
      entries.push({ adventureId, levelId });
    }
  }

  // Generate index.ts
  const indexLines = [
    `// This file is auto-generated by scripts/generate-solutions.mjs. Do not edit by hand.`,
    `import type { Solution } from "./types";`,
    ``,
  ];

  for (const { adventureId, levelId } of entries) {
    const importId = toIdentifier(adventureId, levelId);
    indexLines.push(`import { solution as ${importId} } from "./${adventureId}/${levelId}";`);
  }

  indexLines.push(``);
  indexLines.push(`export const SOLUTIONS: Solution[] = [`);
  for (const { adventureId, levelId } of entries) {
    indexLines.push(`  ${toIdentifier(adventureId, levelId)},`);
  }
  indexLines.push(`];`);
  indexLines.push(``);

  writeFileSync(resolve(SOLUTIONS_DIR, "index.ts"), indexLines.join("\n"), "utf-8");
  console.log(`  Generated solutions/index.ts (${entries.length} solution${entries.length !== 1 ? "s" : ""})`);

  // Patch GENERATED:solutions regions in route/config/test files.
  patchRegion(
    resolve(ROOT, "react-router.config.ts"),
    "// GENERATED:solutions",
    "// /GENERATED:solutions",
    entries
      .map(({ adventureId, levelId }) => `    "/adventures/${adventureId}/levels/${levelId}/solution",`)
      .join("\n") + "\n    "
  );

  patchRegion(
    resolve(ROOT, "src/test/seo.test.ts"),
    "// GENERATED:solutions",
    "// /GENERATED:solutions",
    entries
      .map(({ adventureId, levelId }) => `  "/adventures/${adventureId}/levels/${levelId}/solution",`)
      .join("\n") + "\n  "
  );

  patchRegion(
    resolve(ROOT, "src/test/prerender.test.ts"),
    "// GENERATED:solutions",
    "// /GENERATED:solutions",
    entries
      .map(({ adventureId, levelId }) =>
        [
          `  {`,
          `    file: "adventures/${adventureId}/levels/${levelId}/solution/index.html",`,
          `    check: { type: "contains", value: "Solution:" },`,
          `  },`,
        ].join("\n")
      )
      .join("\n") + "\n  "
  );

  patchRegion(
    resolve(ROOT, "e2e/smoke.spec.ts"),
    "// GENERATED:solutions",
    "// /GENERATED:solutions",
    entries
      .map(({ adventureId, levelId }) =>
        `  { path: "/adventures/${adventureId}/levels/${levelId}/solution", title: /Solution/ },`
      )
      .join("\n") + "\n  "
  );
}

function escapeRegex(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function patchRegion(filePath, openMarker, closeMarker, body) {
  if (!existsSync(filePath)) return;
  const content = readFileSync(filePath, "utf-8");
  const re = new RegExp(
    `(${escapeRegex(openMarker)})[\\s\\S]*?(${escapeRegex(closeMarker)})`
  );
  if (!re.test(content)) return;
  const next = content.replace(re, `$1\n${body}$2`);
  if (next !== content) {
    writeFileSync(filePath, next, "utf-8");
    console.log(`  Patched region: ${filePath.replace(ROOT + "/", "")}`);
  }
}

function toIdentifier(adventureId, levelId) {
  return `solution_${adventureId}_${levelId}`.replace(/-/g, "_");
}

generateSolutions();
